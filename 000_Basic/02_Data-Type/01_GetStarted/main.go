package main

import (
	"fmt"
)

func main() {
	fmt.Println("go" + "lang")    // concatenate 2 strings
	fmt.Println("1 + 2 =", 1+2)   // Integer
	fmt.Println("7 / 3 =", 7.0/3) // floats
	fmt.Println(true && false)    // boolean operator AND
	fmt.Println(true || false)    // boolean operator OR
	fmt.Println(!true)            // boolean operator NOT
}
